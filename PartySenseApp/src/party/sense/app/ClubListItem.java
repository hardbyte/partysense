package party.sense.app;

public class ClubListItem {
	public int ClubBG;
    public String ClubName;
    public String ClubSub;
    public ClubListItem() {
        super();
	}
    
    public ClubListItem(int clubBG, String clubName, String clubSub) {
        super();
        ClubBG = clubBG;
        ClubName = clubName;
        ClubSub = clubSub;
    }
}
